<h5><b>

JAV bendrovė „SpaceX“ pirmadienį pasiuntė atsargų į Tarptautinę kosminę stotį (TKS), paleidusi raketą ir krovinių kapsulę, kartą jau panaudotus per kitas ankstesnes misijas.

</b><h5>
	
	<p style="font-size:20px">
		Ši misija – jau antrasis atvejis, kai Kalifornijoje įkūrusi kompanija pasiuntė į orbitą kartą jau panaudotą raketą „Falcon 9“ ir krovinių kapsulę „Dragon“. 
		<br></br>

		„Falcon 9“ keliauja“, – paskelbė „SpaceX“ tiesioginės vaizdo transliacijos komentatorius, kai balta raketa 16 val. 30 min. vietos (23 val. 30 min. Lietuvos) laiku pradėjo kilti į padangę virš Kanaveralo kyšulio Floridoje. 
		<br></br>

		„SpaceX“ darbuotoja Jessica Jensen, „Dragon“ misijų direktorė, sakė, kad šįkart paleistos „Falcon“ pirmoji pakopa anksčiau buvo panaudota 2017 metų rugsėjį, o krovinių kapsulė į TKS kapsulė „Dragon“ į orbitinę stotį jau skrido 2016 metų balandį.
		<br></br>

		Tokį nenaujų raketos komponentų derinį „SpaceX“ pirmąkart išbandė 2017-ųjų gruodį per kitą JAV kosmoso agentūros NASA užsakymu surengtą TKS aprūpinimo misiją. 
		<br></br>

		„SpaceX“ siekia sumažinti kosminių skrydžių savikainą, ne vieną kartą naudodama milijonus dolerių kainuojančius raketų komponentus, kurie anksčiau po kiekvieno starto paprastai būdavo nuskandinami vandenyne. 
		<br></br>

		„Iš tikrųjų smagu, kad tai tampa įprasta“, – pažymėjo J. Jensen.
		<br></br>

		Pirmadienį įvykęs startas yra 14-oji NASA užsakymu surengta „SpaceX“ krovinių gabenimo misija, vykdoma pagal 1,6 mlrd. dolerių (1,3 mlrd. eurų) kontraktą, turintį užtikrinti orbitoje dirbantiems astronautams reikalingų atsargų ir įrangos tiekimą. 
		<br></br>

		„Dragon“ į TKS gabena apie 2,6 t maisto ir medžiagos moksliniams eksperimentams, tarp jų vienam, susijusiam su audrų tyrimais, taip pat kitam, susijusiam su eksperimentinių vaistų kūrimu kosmose. 
		<br></br>

		Kapsulė su stotimi turėtų susijungti anksti trečiadienį. Ji ten liks apie mėnesį ir galiausiai bus grąžinta į Žemę.


