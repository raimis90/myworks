<div style="margin-left: 10px;">
<p><h5>Kažkada seniai seniai internetas mums asocijavosi su anonimiškumu. Šiandien – priešingai: jaučiame, kad kiekvienas mūsų žingsnis internete yra stebimas. Vos prieš savaitę britų „The Observer“ paviešino informaciją apie tai, kad bendrovė „Facebook“ nesugebėjo užtikrinti dešimčių milijonų naudotojų duomenų apsaugos.</h5></p>

<p>„The Guardian“ technologijų reporteris Alexas Hernas ir apžvalgininkė Arwa Mahdawi parengė naujas naudojimosi internetu taisykles ir kviečia skaitytojus su jomis susipažinti.</p>
<p>1. Parsisiųskite visą informaciją, kurią apie jus turi „Google“
</p>
<p>Tai, ką apie jus turi „Facebook“, galbūt jau parsisiuntėte – šiomis dienomis tai tapo ypač madinga. Dabar atėjo laikas peržiūrėti tai, ką apie jus turi „Google“. Pasinaudokite įrankiu „Google Takeout“ ir parsisiųskite savo duomenis iš įvairių „Google“ šaltinių, kuriais kada nors galbūt esate pasinaudojęs ar naudojatės iki šiol: elektroninio pašto, žemėlapių, paieškos sistemos ir disko. Gausite kelis milžiniško dydžio failus, kuriuose bus sudėta viskas – nuo vaizdo įrašų, kuriuos žiūrėjote per „Youtube“, iki paieškos istorijos ir vietų, kuriose apsilankėte.Pradžiai „The Guardian“ rekomenduoja ištrinti „Google Maps“ istoriją. Tada galbūt norėsite paprašyti „Google“, kad jūsų buvimo vietos daugiau apskritai nebefiksuotų. Jei taip, prisijunkite prie savo „Google“ paskyros, atsidarykite žemėlapius, meniu pasirinkite „Jūsų laiko juosta“. Ekrano apačioje pamatysite „Tvarkyti vietovių istoriją“, ir pirmyn.
</p>
<p>2. Pasistenkite, kad jūsų išmanusis skrudintuvas per daug nelandžiotų į internetą
</p>
<p>Šiais laikais galite įsigyti praktiškai bet kurio prietaiso išmaniąją versiją. Gal turite išmanųjį skrudintuvą, esate personalizavęs duonos skrudinimo nustatymus ir į mobilųjį gaunate žinutę, kai pusryčiai paruošti? Gal naudojatės šakute, kurioje įdiegta „Bluetooth“ funkcija ir kuri, jei valgote per greitai, pradeda vibruoti? Daugelio tokių prietaisų jums tikrai nereikia. Jie ne tik kad per brangūs – jie nėra pakankamai apsaugoti ir gali pridaryti bėdos.
</p>
<p>3. Kontroliuokite savo „AirDrop“ funkciją</p>
<p>Jei naudojatės „iPhone“, būdami viešoje vietoje „AirDrop“ funkciją arba išjunkite, arba bent apribokite iki kontaktų – taip galėsite būti tikri, kad viešajame transporte nepažįstamasis jums neatsiųs ko nors ne itin padoraus arba itin nepadoraus.</p>
<p>4. Pasirūpinkite nebenaudojamos pašto dėžutės saugumu</p>
<p>Turite seniai nenaudojamą elektroninį paštą, kurio panaikinti niekaip neprisiruošiate? Tokia el. pašto dėžutė – tikras jūsų asmeninės informacijos lobynas, tik ir laukiantis, kad kas nors į jį įsilaužtų. Pasikeiskite slaptažodį ir įsijunkite dvipakopę patikrą. Patikrinkite nustatymus ir įsitikinkite, kad nesate užsisakęs kokių nors gretutinių paslaugų (pvz., saugyklos debesijoje).</p>
<p>5. Slaptažodis 1234 – prasta mintis</p>
<p>Netinka ir tokie žodžiai kaip „password“ (slaptažodis) arba „monkey“ (beždžionė) – pastarasis, kaip rodo statistika, kažkodėl yra vienas populiariausių slaptažodžių. Saugiausi slaptažodžiai yra tie, kuriuos sudaro daug simbolių, todėl, galvodami slaptažodį, geriau galvokite ne apie pavienį žodį, o apie visą frazę, pavyzdžiui, „toksslaptažodiskadniekasneatspės“.</p>
<p>6. Pasitikrinkite, ar jūsų pašto niekas nepasisavino</p>
<p>Anglai sakytų „pwned“. Tai, be kita ko, reikštų, kad į jūsų elektroninio pašto paskyrą buvo įsilaužta ir dabar jūs nebesate vienintelis jo šeimininkas. Ar taip nėra, patariama tikrinti reguliariai. Apsilankykite svetainėje haveibeenpwned.com, įveskite savo el. pašto adresą ir sužinosite, ar jūsų duomenimis niekas nesinaudoja. Nustačius pažeidimą, reikia nedelsiant reaguoti, pavyzdžiui, pasikeisti slaptažodį.</p>
<p>7. Atsargiai – personalizuota kainodara</p>
<p>Visi esame girdėję apie dinamišką kainodarą – tą erzinantį dalyką, kai skrydžių kainas nulemia pasiūlos ir paklausos svyravimai. Dabar vis dažniau girdime apie personalizuotą kainodarą – tokią, kai prekybininkai, išanalizavę jūsų duomenis, patys nusprendžia, kiek jūs būtumėte linkęs sumokėti už prekę ar paslaugą. Taigi, prieš įsigydami brangų pirkinį internetu, pamėginkite jo kainą patikrini keliuose įrenginiuose, taip pat pažiūrėkite, ar kaina tebėra tokia pati įsijungus anoniminį arba privatų režimą. Kartais kainą lemia ir buvimo vieta, todėl, jei manote, kad to reikia, galite parsisiųsti įrankių, kurie leis jums suklastoti savo buvimo vietą.</p>
<p>8. Pamojuokite saugumo pareigūnui, stebinčiam jus per jūsų įtaiso internetinę vaizdo kamerą</p>
<p>Net ir šnipams kartais norisi pabendrauti.
</p>
<p>9. Jums nereikia jokių pranešimų, išskyrus tuos, kurie įspėja, kad su jumis nori tiesiogiai susisiekti kitas asmuo</p>
<p>Atsisakyti pranešimų nėra labai jau sudėtinga. Žaidžiate vienam žaidėjui skirtą žaidimą? Tuomet neturėtumėte gauti apskritai jokių pranešimų. „Instagram“ šiukšlės: „Žinomas šuo ką tik pasidalino nauja nuotrauka“ – ir tokius pranešimus galima atjungti, žinoma, tektų gerokai panaršyti nustatymuose. Ar gali būti išimčių? Gali. Galbūt jums tikrai pravers pranešimas apie tai, kad būtent dabar – nepakartojama proga pasimokyti ispanų kalbos.
</p>
<p>10. Būkite protingi, kai naudojatės viešuoju internetu</p>
<p>Nieko baisaus tikriausiai neatsitiks, jei nuotraukomis pasidalysite jas įkeldami į bendrinamą arba privatų nuotraukų albumą. Tačiau, jei interneto prieiga vieša, „Google“ jį susiras. O kai „Google“ jį susiras, jis ten liks amžinai.</p>
<p>11. Telefoną laikykite kišenėje arba bent jau pasidėkite ekranu žemyn
</p>
<p>Ypač – kai esate su draugais. Nebent norite jiems aiškiai parodyti, kad mieliau bendrautumėte ne su jais, o su kažkuo telefono ekrane.</p>
<p>12. Kartais pravartu viską pradėti iš naujo</p>
<p>Jei bent kartą per metus telefoną „išsivalote“, tikriausiai jau pastebėjote, kad vienos programėlės tikrai reikalingos, o kitos tik užima vietą ir siurbia jūsų duomenis. Pagalvokite, galbūt vertėtų kasmet atsinaujinti ir savo „Facebook“ paskyrą – taip pagaliau patys pamatytumėte, kurie draugai iš tiesų yra draugai, o kurie – tik jūsų socialinio gyvenimo siurbėlės.</p>
<p>13. „Amazon Echo“ – nieko tokio, bet kameros miegamajame nenaudokite</p>
<p>Čia nereikia didelės analizės.</p>
<p>14. Diena be alkoholio – diena be socialinių tinklų</p>
<p>Pamėginkite pabūti atsijungę nuo socialinių tinklų tiek pat dienų, kiek galite išbūti nevartoję alkoholio.</p>
<p>15. Išmokykite smegenis išlaikyti dėmesį</p>
<p>Išsisaugokite ilgesnius tekstus naudodamiesi „Instapaper“ arba „Pocket“ ir skaitykite juos neatsitraukdami. Atsidarykite keturių tūkstančių žodžių straipsnį apie vėžlius ir perkaitykite jį vienu ypu. Jei pavyks, pabandykite atsiversti knygą.</p>
<p>16. Neleiskite algoritmams spręsti, ką turite veikti</p>
<p>Jūs – ne robotas, jūs – žmogus. Tai, kad laisva valia galite priimti sprendimus, yra didžiausias jūsų privalumas. Kai per „Youtube“ žiūrite vaizdo įrašą ir jis pasibaigia, nežiūrėkite to kito, kurį algoritmas jums pasiūlo automatiškai. Kai iš ryto pasiimate į rankas telefoną, nepradėkite nuo naujienų, kurias „Apple News“ ar „Google Now“ jums pasiūlys pirmąsias.</p>
<p>17. Su savo duomenimis darykite, ką norite, bet draugų duomenis palikite ramybėje
</p>
<p>Gerai pagalvokite, prieš sutikdami, kad kažkokia linksma programėlė įgytų prieigą prie jūsų nuotraukų galerijos ir informacijos apie jūsų buvimo vietą. Jei vis dėlto sutiksite – tai jūsų sprendimas, kurį priimti jūs turite visas teises. Tačiau jūsų draugų duomenys – ne jūsų, o jų nuosavybė. Todėl gerai pagalvokite, prieš leisdami programėlei prieiti prie jūsų adresų knygelės ar draugų profilių.</p>
<p>18. Pagaliau atminkite, kad privatumas – saugotinas dalykas</p>
<p>Galbūt sakysite, kad neturite, ką slėpti, tačiau tai dar nereiškia, kad jūsų privatumas jums neturi rūpėti. Visi turime suprasti, kad vis daugiau mūsų gyvenimo detalių virsta duomenų taškais. Net ir menkiausi mūsų veiksmai kažkam reiškia pinigus. Kai pradedame suvokti, kad esame nuolat stebimi, pradedame keistis. Tai daro įtaką tam, kokias prekes renkamės ir kokias idėjas priimame. Atsakingai ir protingai elgtis internetinėje erdvėje svarbu norint apsaugoti ne tik savo informaciją, bet ir save pačius.</p>

