<p>1842</p>	

<p>Matematinį išsilavinimą turėjusi (tais laikais tai buvo retenybė) Ada Lavleis užrašė komentarą, kuris vėliau tapo programavimo mokslo pagrindu.</p>

<p>1936</p>

<p>Šiais metais Alanas Tiuringas, laikomas informatikos tėvu, pasiūlė algoritmais paremtos skaičiavimo mašinos idėją. Ji iki šiol yra laikoma šiuolaikinių kompiuterių pagrindu.</p>

<p>1957</p>

<p>Džonas Bekus kartu su įmonės IBM tyrinėtojų grupe sukūrė vieną pirmųjų ir iki šiol naudojamų algoritminių kalbų FORTRAN. Ši kalba išsiskyrė tuo, kad ji buvo aiški ir lengvai suprantama.</p>

<p>1959</p>

<p>Greisė Hoper sukūrė finansų institucijoms skirtą programavimo kalbą ir pavadino ją COBOL (angliškai šifruojama kaip „Common Business-Oriented Language“). Ši kalba naudojama iki šiol.</p>

<p>1964</p>

<p>Džonas Kemenis ir Tomas Kurtzas sukūrė programavimo kalbą pradedantiesiems, kurią pavadino BASIC. Šis pavadinimas angliškai šifruojamas kaip „Beginners Allpurpose Symbolic Instruction Code“, kas reiškia pradedančiųjų daugiatikslis simbolinių instrukcijų kodas.</p>

<p>1970</p>

<p>Niklausas Virtas sukuria programavimo kalbą „Pascal“ kaip priemonę, kuri paskatintų studentus programuoti struktūriškai. Ši kalba iki šiol naudojama mokyti programavimo kalbų pagrindų.</p>

<p>1972</p>

<p>Denis Ričis sukūrė programavimo kalbą C. Už tai jis gavo Nacionalinį technologijų medalį!</p>

<p>1980</p>

<p>Alanas Kėjus sukuria objektiškai orientuotą programavimo kalbą „Smalltalk“.</p>

<p>1987</p>

<p>Laris Volas – profesionalus programuotojas ir hakeris – sukuria programavimo kalbą „Perl“.</p>

<p>1983</p>

<p>Žanas Ichbijas sukuria programavimo kalbą ADA (taip, Ados Lavleis garbei).</p>

<p>1983</p>

